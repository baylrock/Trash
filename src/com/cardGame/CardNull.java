public class CardNull {

    String question = "0",
            answer = "0";


    public CardNull(String question, String answer) {
        this.question = question;
        this.answer = answer;


    }

    public String getQuestion() {
        return question;
    }

    public String getAnswer() {
        return answer;
    }
}

